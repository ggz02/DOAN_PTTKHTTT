    private void addPNBTNActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addPNBTNActionPerformed
        // TODO add your handling code here:
                String msPN = "";
                String splitSoPN ="";
                int newSoPNInt;
                String newSoPNString ="";
                ArrayList<PhieuNhap> listNewSoPN = PhieuNhapDAO.getInstance().selectSoPN();
                for (PhieuNhap pn : listNewSoPN) {
                    msPN = pn.getSoPN();
                }
                splitSoPN = msPN.substring(2);
                newSoPNInt = Integer.valueOf(splitSoPN) +1;
                if (newSoPNInt < 10) {
                   newSoPNString = "PN00" + Integer.toString(newSoPNInt);
                } else if (newSoPNInt >= 10) {
                   newSoPNString = "PN0" + Integer.toString(newSoPNInt);
                }

//                
//                
                String MaNCC = inputMaNCC.getText();
                String MaTT = inputMaTT.getText();
                

                Calendar date1 = inputNgayTao.getCalendar();
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                String NgayTao = dateFormat.format(date1.getTime()); 

                
                Calendar date2 = inputNgayNhap.getCalendar();
                SimpleDateFormat dateFormat2 = new SimpleDateFormat("yyyy-MM-dd");
                String NgayNhap = dateFormat2.format(date2.getTime()); 

                
                
                PhieuNhap pn = new PhieuNhap(newSoPNString, MaNCC, MaTT, NgayTao, NgayNhap);
                try {
                PhieuNhapDAO.getInstance().insert(pn);
                JOptionPane.showMessageDialog(null, "Thêm phiếu nhập thành công");  
                defUI2();
                } catch (Exception ex) {
                   ex.printStackTrace();

                }


                

                int rowCount = listTableAdd.getRowCount();
                int columnCount = listTableAdd.getColumnCount();
                String ISBN = "";
                int NamXB = 0;
                int SoLuong = 0;
                int GiaNhap = 0;
                for (int rowIndex = 0; rowIndex < rowCount; rowIndex++) {
                    for (int columnIndex = 0; columnIndex < columnCount; columnIndex++) {
                        Object value = listTableAdd.getValueAt(rowIndex, columnIndex);
                if (columnIndex == 0) {
                    ISBN = (String) value;
                } 
                if (columnIndex == 2) {
                    if (value instanceof Integer) {
                        NamXB = (int) value;
                    } else if (value instanceof String) {
                        NamXB = Integer.valueOf((String) value);
                    }
                }
                if (columnIndex == 3) {
                    if (value instanceof Integer) {
                        SoLuong = (int) value;
                    } else if (value instanceof String) {
                        SoLuong = Integer.valueOf((String) value);
                    }
                }
                if (columnIndex == 4) {
                    if (value instanceof Integer) {
                        GiaNhap = (int) value;
                    } else if (value instanceof String) {
                        GiaNhap = Integer.valueOf((String) value);
                    }
                }
                    }

                    CTPhieuNhap ctpn = new CTPhieuNhap(newSoPNString, ISBN, NamXB, SoLuong, GiaNhap);
                    try {
                        CTPhieuNhapDAO.getInstance().insert(ctpn);
                        } catch (Exception ex) {
                           ex.printStackTrace();

                        }
                }
                tableModel.setRowCount(0);
                

                
                
    }//GEN-LAST:event_addPNBTNActionPerformed

