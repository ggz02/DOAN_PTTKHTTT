    private void upBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_upBtnActionPerformed
        // TODO add your handling code here:
 
         
         
     if(upBtn.getText().equalsIgnoreCase("Sửa")) {
        if(listTableAdd.getSelectedRowCount() != 1) {
          JOptionPane.showMessageDialog(null, "Vui lòng chọn hàng muốn Sửa"); 
        } else {
         saveBtn.setVisible(true);
         upBtn.setText("Hủy");
         addBtn.setEnabled(false);
         delBTN.setEnabled(false);
         
         
         String chooseISBN = tableModel.getValueAt(listTableAdd.getSelectedRow(), 0).toString();
         String chooseTenDS =tableModel.getValueAt(listTableAdd.getSelectedRow(), 1).toString();
         String chooseNamXB =tableModel.getValueAt(listTableAdd.getSelectedRow(), 2).toString();
         String chooseSoLuong =tableModel.getValueAt(listTableAdd.getSelectedRow(), 3).toString();


         valueSP.setText(chooseSoLuong);

//
        for (int i = 0; i < listDauSach.getItemCount(); i++) {
            String item = listDauSach.getItem(i);
            if(item.equalsIgnoreCase(chooseTenDS)){
                listDauSach.select(i);
            }
        }
        
         for (int i = 0; i < listNamXB.getItemCount(); i++) {
            String item = listNamXB.getItem(i);
            if(item.equalsIgnoreCase(chooseNamXB)){
                listNamXB.select(i);
            }
         }
         
         int[] selectedRows = listTableAdd.getSelectedRows();
            if (selectedRows.length > 0) {
            for (int i = selectedRows.length - 1; i >= 0; i--) {
                tableModel.removeRow(selectedRows[i]);
            }
        }
         
         
         } 
     }
     else {
         saveBtn.setVisible(false);
         upBtn.setText("Sửa");
         addBtn.setEnabled(true);
         delBTN.setEnabled(true); 
         JOptionPane.showMessageDialog(null, "Hủy Sửa thành công");

         }
    
    }//GEN-LAST:event_upBtnActionPerformed

