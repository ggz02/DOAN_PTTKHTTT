    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(check) {
            
                String msSach = "";
                String splitMaSach ="";
                int newMaSachInt;
                String newMaSachString ="";
                ArrayList<Sach> listNewMaSach = SachDAO.getInstance().selectNewMaSach();
                for (Sach s : listNewMaSach) {
                    msSach = s.getMaSach();
                }
                splitMaSach = msSach.substring(1);
                newMaSachInt = Integer.valueOf(splitMaSach) +1;
                if (newMaSachInt < 10) {
                   newMaSachString = "S000" + Integer.toString(newMaSachInt);
                } else if (newMaSachInt > 10) {
                   newMaSachString = "S00" + Integer.toString(newMaSachInt);
                }
                System.out.println(newMaSachString);
            //                inputMa.setEnabled(true);
            int NamXB = Integer.parseInt(inputNamXB.getText());
            String Gia = inputGiaSach.getText();
            String GhiChu = inputGhiChu.getText();
            Sach s = new Sach(newMaSachString,ISBNChose,Integer.parseInt(Gia),NamXB,GhiChu);

            try {
                SachDAO.getInstance().insert(s);
                JOptionPane.showMessageDialog(null, "Thêm Sách thành công");
                showData();
                btnAdd.setText("Thêm");
                cleanUI();
            } catch (Exception e) {
                e.printStackTrace();
            }
            
            //
        }
        //
        else {
            String MaSach = inputMaSach.getText();
            String NamXB = inputNamXB.getText();
            String Gia = inputGiaSach.getText();
            String GhiChu = inputGhiChu.getText();
            Sach s = new Sach(MaSach,ISBNChose,Integer.parseInt(Gia),Integer.parseInt(NamXB),GhiChu);

            try {
                SachDAO.getInstance().update(s);
                JOptionPane.showMessageDialog(null, "Sửa Sách thành công");
                showData();
                btnUp.setText("Sửa");
                cleanUI();

            } catch (Exception ex) {
                ex.printStackTrace();

            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        defUI();
        if (btnAdd.getText().equalsIgnoreCase("Thêm")) {
            defUI();
            inputISBN.setEnabled(false);
            inputMaSach.setEnabled(false);
            btnAdd.setText("Hủy");
            btnDel.setEnabled(false);
            btnUp.setEnabled(false);
            btnSave.setVisible(true);
            check = true;
        } else {
            cleanUI();
            btnAdd.setText("Thêm");
            btnDel.setEnabled(true);
            btnUp.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Hủy thêm Sách thành công");
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpActionPerformed
        // TODO add your handling code here:
        if (btnUp.getText().equalsIgnoreCase("Sửa")) {
            if(table.getSelectedRowCount() != 1) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn hàng muốn sửa");
            }
            else {
               inputISBN.setEnabled(false);
                btnUp.setText("Hủy");
                btnAdd.setEnabled(false);
                btnDel.setEnabled(false);
                btnSave.setEnabled(true);
                inputISBN.setEnabled(false);
                inputMaSach.setEnabled(true);
                inputGiaSach.setEnabled(true);
                inputNamXB.setEnabled(true);
                inputGhiChu.setEnabled(true);
                btnSave.setVisible(true);
                check = false;
            }
        }
        else {
            cleanUI();
            btnUp.setText("Sửa");
            JOptionPane.showMessageDialog(null, "Hủy sửa Sách thành công");
        }
    }//GEN-LAST:event_btnUpActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if(table.getSelectedRowCount() != 1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn hàng muốn Xóa");
        }
        else {
            String MaSach = inputMaSach.getText();
            String NamXB = inputNamXB.getText();
            String Gia = inputGiaSach.getText();
            String GhiChu = inputGhiChu.getText();
            Sach s = new Sach(MaSach,ISBNChose,Integer.parseInt(Gia),Integer.parseInt(NamXB),GhiChu);

            try {
                SachDAO.getInstance().delete(s);
                JOptionPane.showMessageDialog(null, "Xóa Sách thành công");
                showData();
                cleanUI();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_btnDelActionPerformed

