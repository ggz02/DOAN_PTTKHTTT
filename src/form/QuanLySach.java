    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        if(choiceSearchMaS.isSelected()){
            String search = inputSearch.getText();
            listS = SachDAO.getInstance().selectByCondition(search,"", 1);
            tableModel.setRowCount(0);
            listS.forEach((s) -> {
                tableModel.addRow(new Object[] {s.getISBN(), s.getMaSach(), s.getNamXB(),s.getGia(),s.getGhiChu()});

            });
        }
        else if(choiceSearchNamXB.isSelected()){
            String search = inputSearch.getText();
            listS = SachDAO.getInstance().selectByCondition(search,ISBNChose, 2);
            tableModel.setRowCount(0);
            listS.forEach((s) -> {
                tableModel.addRow(new Object[] {s.getISBN(), s.getMaSach(), s.getNamXB(),s.getGia(),s.getGhiChu()});

            });
        }
        else {
           JOptionPane.showMessageDialog(null, "Vui lòng chọn Hình Thức Bạn Muốn Tìm Theo");
            showData(); 
        }

//
        if (table.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "Không có dữ liệu");
            showData();
        }
        buttonGroup1.clearSelection();
        inputSearch.setText("");
    }//GEN-LAST:event_searchBtnActionPerformed

