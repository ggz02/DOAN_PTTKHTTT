    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(check) {
//                inputMa.setEnabled(true);
                String msDG = "";
                String splitMaDG ="";
                int newMaDGInt;
                String newMaDGString ="";
                ArrayList<DocGia> listNewMaDG = DocGiaDAO.getInstance().selectNewMaDG();
                for (DocGia docGia : listNewMaDG) {
                    msDG = docGia.getMaDG();
                }
                splitMaDG = msDG.substring(2);
                newMaDGInt = Integer.valueOf(splitMaDG) +1;
                if (newMaDGInt > 10) {
                   newMaDGString = "DG0" + Integer.toString(newMaDGInt);
                } else if (newMaDGInt > 100) {
                   newMaDGString = "DG" + Integer.toString(newMaDGInt);
                }
                String HoTen = inputTen.getText();
                Date date = inputNS.getDate();  
                String NgaySinh = DateFormat.getDateInstance().format(date);
                String GioiTinh = "";
                if(btnNam.isSelected()) {
                    GioiTinh = "Nam";
                } else {
                    GioiTinh = "Nữ";
                }
                String DienThoai = inputDT.getText();
                String DiaChi = inputDC.getText();
                String DoiTuong = "";
                if (listDT.getSelectedIndex() == 1) {
                    DoiTuong = "Sinh viên";
                } else if (listDT.getSelectedIndex() == 2) {
                    DoiTuong = "Giảng viên";
                }
                DocGia DG1 = new DocGia(newMaDGString,HoTen,NgaySinh,GioiTinh,DienThoai,DiaChi,DoiTuong);
                
            try {
                DocGiaDAO.getInstance().insert(DG1);
                JOptionPane.showMessageDialog(null, "Thêm Độc Giả thành công");
                showData(); 
                btnAdd.setText("Thêm");
                cleanUI();
            } catch (Exception e) {
                    e.printStackTrace();
                 }
              
           
        }
        
        else {
                String msDG = inputMa.getText();
                String HoTen = inputTen.getText();
                Date date = inputNS.getDate();  
                String NgaySinh = DateFormat.getDateInstance().format(date);
                String GioiTinh = "";
                if(btnNam.isSelected()) {
                    GioiTinh = "Nam";
                } else {
                    GioiTinh = "Nữ";
                }
                String DienThoai = inputDT.getText();
                String DiaChi = inputDC.getText();
                String DoiTuong = "";
                if (listDT.getSelectedIndex() == 1) {
                    DoiTuong = "Sinh viên";
                } else if (listDT.getSelectedIndex() == 2) {
                    DoiTuong = "Giảng viên";
                }
                DocGia DG1 = new DocGia(msDG,HoTen,NgaySinh,GioiTinh,DienThoai,DiaChi,DoiTuong);
            
            try {
                DocGiaDAO.getInstance().update(DG1);
                JOptionPane.showMessageDialog(null, "Sửa Độc Giả thành công");  
                showData();
                btnUp.setText("Sửa");     
                cleanUI();
                
            } catch (Exception ex) {
               ex.printStackTrace();

            }
        }   
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if(table.getSelectedRowCount() != 1) {
          JOptionPane.showMessageDialog(null, "Vui lòng chọn hàng muốn Xóa"); 
        }
        else {
                String msDG = inputMa.getText();
                String HoTen = inputTen.getText();
                Calendar date = inputNS.getCalendar();
                SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
                String NgaySinh = dateFormat.format(date.getTime());
                String GioiTinh = "";
                if(btnNam.isSelected()) {
                    GioiTinh = "Nam";
                } else {
                    GioiTinh = "Nữ";
                }
                String DienThoai = inputDT.getText();
                String DiaChi = inputDC.getText();
                String DoiTuong = "";
                if (listDT.getSelectedIndex() == 1) {
                    DoiTuong = "Sinh viên";
                } else if (listDT.getSelectedIndex() == 2) {
                    DoiTuong = "Giảng viên";
                }
                DocGia DG1 = new DocGia(msDG,HoTen,NgaySinh,GioiTinh,DienThoai,DiaChi,DoiTuong);
            try {
                DocGiaDAO.getInstance().delete(DG1);
                JOptionPane.showMessageDialog(null, "Xóa Độc giả thành công");  
                showData();
                cleanUI();  
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
        
    }//GEN-LAST:event_btnDelActionPerformed

    private void btnUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpActionPerformed
        // TODO add your handling code here:
        if (btnUp.getText().equalsIgnoreCase("Sửa")) {
            if(table.getSelectedRowCount() != 1) {
              JOptionPane.showMessageDialog(null, "Vui lòng chọn hàng muốn sửa"); 
            }
            else {
            btnUp.setText("Hủy");
            btnAdd.setEnabled(false);
            btnDel.setEnabled(false);
            btnSave.setEnabled(true);
            inputMa.setEnabled(false);
            inputTen.setEnabled(true);
            inputDC.setEnabled(true);
            inputDT.setEnabled(true);
            inputNS.setEnabled(true);
            btnNam.setEnabled(true);
            btnNu.setEnabled(true);
            listDT.setEnabled(true);
            btnSave.setVisible(true);
            check = false;  
            }
        }
        else {
            cleanUI();
            btnUp.setText("Sửa");
            JOptionPane.showMessageDialog(null, "Hủy sửa Độc giả thành công"); 
        }
        
        
    }//GEN-LAST:event_btnUpActionPerformed

