    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:

        
        String tblMaDG = tableModel.getValueAt(table.getSelectedRow(), 0).toString();
        String tblHoTenDG = tableModel.getValueAt(table.getSelectedRow(), 1).toString();
        String tblNgaySinhDG = tableModel.getValueAt(table.getSelectedRow(), 2).toString();
        String tblGioiTinhDG = tableModel.getValueAt(table.getSelectedRow(), 3).toString();
        String tblDiaChiDG = tableModel.getValueAt(table.getSelectedRow(), 5).toString();
        String tblDienThoaiDG = tableModel.getValueAt(table.getSelectedRow(), 4).toString();
        String tblDoiTuongDG = tableModel.getValueAt(table.getSelectedRow(), 6).toString();

        inputMa.setText(tblMaDG);
        inputTen.setText(tblHoTenDG); 
       
        try {
          Date d = new SimpleDateFormat("yyyy-MM-dd").parse(tblNgaySinhDG);
            inputNS.setDate(d);  
        } catch (Exception e) {
            e.printStackTrace();
        }
        
        if (tblGioiTinhDG.equalsIgnoreCase("Nam")) {
            btnNam.setSelected(true);
        } else {
            btnNu.setSelected(true);
        }
        inputDT.setText(tblDienThoaiDG);
        inputDC.setText(tblDiaChiDG);
        if (tblDoiTuongDG.equalsIgnoreCase("Sinh viên")) {
            listDT.setSelectedIndex(1);
        } else {
            listDT.setSelectedIndex(2);
        }
    }//GEN-LAST:event_tableMouseClicked

