    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:
        String tblISBN = tableModel.getValueAt(table.getSelectedRow(), 0).toString();
        String tblTenLoai = tableModel.getValueAt(table.getSelectedRow(), 1).toString();
        String tblTenSach = tableModel.getValueAt(table.getSelectedRow(), 2).toString();
        String tblTacGia = tableModel.getValueAt(table.getSelectedRow(), 3).toString();
        String tblNXB = tableModel.getValueAt(table.getSelectedRow(), 4).toString();
        String tblSoLuong = tableModel.getValueAt(table.getSelectedRow(), 5).toString();
        String tblThongTin = tableModel.getValueAt(table.getSelectedRow(), 6).toString();


        inputISBN.setText(tblISBN);
        for (int i = 0; i < choiceTenLoai.getItemCount(); i++) {
            if(tblTenLoai.equalsIgnoreCase(choiceTenLoai.getItem(i))) {
                choiceTenLoai.select(i);
            }
        }
        
        inputTenSach.setText(tblTenSach);
        inputTacGia.setText(tblTacGia);
        inputNXB.setText(tblNXB);
        inputSoLuong.setText(tblSoLuong);
        inputThongTinSach.setText(tblThongTin);
       
    }//GEN-LAST:event_tableMouseClicked

    private void btnUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpActionPerformed
        // TODO add your handling code here:
        if (btnUp.getText().equalsIgnoreCase("Sửa")) {
            if(table.getSelectedRowCount() != 1) {
              JOptionPane.showMessageDialog(null, "Vui lòng chọn hàng muốn sửa"); 
            }
            else {
            btnUp.setText("Hủy");
            btnAdd.setEnabled(false);
            btnDel.setEnabled(false);
            btnSave.setEnabled(true);
            inputISBN.setEnabled(false);
            inputTacGia.setEnabled(true);
            inputTenSach.setEnabled(true);
            inputSoLuong.setEnabled(true);
            inputThongTinSach.setEnabled(true);
            inputNXB.setEnabled(true);
            choiceTenLoai.setEnabled(true);
            btnSave.setVisible(true);
            check = false;  
            }
        }
        else {
            cleanUI();
            btnUp.setText("Sửa");
            JOptionPane.showMessageDialog(null, "Hủy sửa Đầu Sách thành công");
            choiceTenLoai.select(0);
        }
    }//GEN-LAST:event_btnUpActionPerformed

