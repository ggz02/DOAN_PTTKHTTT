        public void showData(){
         listS = SachToHopDAO.getInstance().selectAll();
         tableModel.setRowCount(0);
         listS.forEach((s) -> {
            tableModel.addRow(new Object[] {s.getISBN(), s.getTenLoai(), s.getTenSach(),s.getTacGia(),s.getNXB(),s.getSoluong(),s.getThongTinSach()});
        });

    }
        
    public void showListChoiceTenLoai(){
        choiceTenLoai.add("");
        ArrayList<TheLoai> listT1 = TheLoaiDAO.getInstance().selectTenLoaiSach("",1);
        for (TheLoai theLoai : listT1) {
            choiceTenLoai.add(theLoai.getTenLoai());
        }
        
    }    
        
