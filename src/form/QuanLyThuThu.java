    private void tableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableMouseClicked
        // TODO add your handling code here:

        String tblMaTT = tableModel.getValueAt(table.getSelectedRow(), 0).toString();
        String tblHoTenTT = tableModel.getValueAt(table.getSelectedRow(), 1).toString();
        String tblNgaySinhTT = tableModel.getValueAt(table.getSelectedRow(), 2).toString();
        String tblGioiTinhTT = tableModel.getValueAt(table.getSelectedRow(), 3).toString();
        String tblDiaChiTT = tableModel.getValueAt(table.getSelectedRow(), 5).toString();
        String tblDienThoaiTT = tableModel.getValueAt(table.getSelectedRow(), 4).toString();

        inputMa.setText(tblMaTT);
        inputTen.setText(tblHoTenTT);

        try {
            Date d = new SimpleDateFormat("yyyy-MM-dd").parse(tblNgaySinhTT);
            inputNS.setDate(d);
        } catch (Exception e) {
            e.printStackTrace();
        }

        if (tblGioiTinhTT.equalsIgnoreCase("Nam")) {
            btnNam.setSelected(true);
        } else {
            btnNu.setSelected(true);
        }
        inputDT.setText(tblDienThoaiTT);
        inputDC.setText(tblDiaChiTT);
    }//GEN-LAST:event_tableMouseClicked

