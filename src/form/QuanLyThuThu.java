    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(check) {
            String msTT = "";
            String splitMaTT ="";
            int newMaTTInt;
            String newMaTTString ="";
            ArrayList<ThuThu> listNewMaTT = ThuThuDAO.getInstance().selectNewMaTT();
            for (ThuThu tt : listNewMaTT) {
                msTT = tt.getMaTT();
            }
            splitMaTT = msTT.substring(2);
            newMaTTInt = Integer.valueOf(splitMaTT) +1;
            if (newMaTTInt < 10) {
                newMaTTString = "TT00" + Integer.toString(newMaTTInt);
            } else if (newMaTTInt > 10) {
                newMaTTString = "TT0" + Integer.toString(newMaTTInt);
            }
            String HoTen = inputTen.getText();
            Date date = inputNS.getDate();
            String NgaySinh = DateFormat.getDateInstance().format(date);
            String GioiTinh = "";
            if(btnNam.isSelected()) {
                GioiTinh = "Nam";
            } else {
                GioiTinh = "Nữ";
            }
            String DienThoai = inputDT.getText();
            String DiaChi = inputDC.getText();

            ThuThu tt = new ThuThu(newMaTTString,HoTen,NgaySinh,GioiTinh,DienThoai,DiaChi);

            try {
                ThuThuDAO.getInstance().insert(tt);
                JOptionPane.showMessageDialog(null, "Thêm Thủ Thư thành công");
                showData();
                btnAdd.setText("Thêm");
                cleanUI();
            } catch (Exception e) {
                e.printStackTrace();
            }

        }

        else {
            String msTT = inputMa.getText();
            String HoTen = inputTen.getText();
            Date date = inputNS.getDate();
            String NgaySinh = DateFormat.getDateInstance().format(date);
            String GioiTinh = "";
            if(btnNam.isSelected()) {
                GioiTinh = "Nam";
            } else {
                GioiTinh = "Nữ";
            }
            String DienThoai = inputDT.getText();
            String DiaChi = inputDC.getText();

            ThuThu tt = new ThuThu(msTT,HoTen,NgaySinh,GioiTinh,DienThoai,DiaChi);

            try {
                ThuThuDAO.getInstance().update(tt);
                JOptionPane.showMessageDialog(null, "Sửa Thủ Thư thành công");
                showData();
                btnUp.setText("Sửa");
                cleanUI();

            } catch (Exception ex) {
                ex.printStackTrace();

            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:

        if(btnAdd.getText().equalsIgnoreCase("Thêm")) {
            defUI();
            btnAdd.setText("Hủy");
            btnDel.setEnabled(false);
            btnUp.setEnabled(false);
            btnSave.setVisible(true);
            check = true;
        } else {
            cleanUI();
            btnDel.setEnabled(true);
            btnUp.setEnabled(true);
            JOptionPane.showMessageDialog(null, "Hủy thêm Thủ Thư thành công");
        }

    }//GEN-LAST:event_btnAddActionPerformed

    private void btnUpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpActionPerformed
        // TODO add your handling code here:
        if (btnUp.getText().equalsIgnoreCase("Sửa")) {
            if(table.getSelectedRowCount() != 1) {
                JOptionPane.showMessageDialog(null, "Vui lòng chọn hàng muốn sửa");
            }
            else {
                btnUp.setText("Hủy");
                btnAdd.setEnabled(false);
                btnDel.setEnabled(false);
                btnSave.setEnabled(true);
                inputMa.setEnabled(false);
                inputTen.setEnabled(true);
                inputDC.setEnabled(true);
                inputDT.setEnabled(true);
                inputNS.setEnabled(true);
                btnNam.setEnabled(true);
                btnNu.setEnabled(true);
                btnSave.setVisible(true);
                check = false;
            }
        }
        else {
            cleanUI();
            btnUp.setText("Sửa");
            JOptionPane.showMessageDialog(null, "Hủy sửa Thủ Thư thành công");
        }

    }//GEN-LAST:event_btnUpActionPerformed

    private void btnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDelActionPerformed
        // TODO add your handling code here:
        if(table.getSelectedRowCount() != 1) {
            JOptionPane.showMessageDialog(null, "Vui lòng chọn hàng muốn Xóa");
        }
        else {
            String msDG = inputMa.getText();
            String HoTen = inputTen.getText();
            Calendar date = inputNS.getCalendar();
            SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd");
            String NgaySinh = dateFormat.format(date.getTime());
            String GioiTinh = "";
            if(btnNam.isSelected()) {
                GioiTinh = "Nam";
            } else {
                GioiTinh = "Nữ";
            }
            String DienThoai = inputDT.getText();
            String DiaChi = inputDC.getText();
            String DoiTuong = "";

            ThuThu tt = new ThuThu(msDG,HoTen,NgaySinh,GioiTinh,DienThoai,DiaChi);
            try {
                ThuThuDAO.getInstance().delete(tt);
                JOptionPane.showMessageDialog(null, "Xóa Thủ Thư thành công");
                showData();
                cleanUI();
            } catch (Exception e) {
                e.printStackTrace();
            }
        }

    }//GEN-LAST:event_btnDelActionPerformed

